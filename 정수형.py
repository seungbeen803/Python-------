# 정수형

# 더하기
print(8 + 5) # 13

# 빼기
print(8 - 5) # 3

# 곱하기
print(8 * 5) # 40

# 나누기
print(8 / 5) # 1.6

# 나눗셈의 몫 구하기
print(8 // 5) # 1

# 나눗셈의 나머지 구하기
print(8 % 5) # 3

# 거듭제곱
print(8 ** 5) # 32768

age = 18
print(age) # 18

after_2 = 2
print(age + after_2) # 20

result = age - after_2
print(result) # 16

age = 18
age += 2 # 20
age -= 1 # 19
age *= 2 # 38
age /= 2 # 19.0
age //= 2 # 9.0
